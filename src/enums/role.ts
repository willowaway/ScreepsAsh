/**
 * The `Role` enum represents the various roles that a creep can have within the game.
 *
 * It is used to assign and distinguish between the different tasks and behaviors that creeps will exhibit.
 */

export enum Role {
	Starter = 0,
	Harvester = 1,
	Hauler = 2,
	Upgrader = 3,
	Builder = 4,
	Repairer = 5,
	Patcher = 6,
	Forager = 7,
	Claimer = 8
}
